//! # 进程管理相关系统调用
//!
//! 实现与进程生命周期管理相关的系统调用，包括进程退出、
//! CPU 时间片让出和系统时间获取等功能。

use crate::println;
use crate::task::{change_program_brk, exit_current_and_run_next, suspend_current_and_run_next};
use crate::timer::get_time_ms;

/// 系统调用：进程退出
///
/// 实现 `exit(2)` 系统调用，终止当前正在运行的应用程序。
/// 该函数会清理当前任务的资源，并调度下一个就绪任务运行。
///
/// ## Arguments
///
/// * `exit_code` - 进程退出码，用于向父进程或系统报告执行状态
///   - 0 通常表示成功完成
///   - 非零值通常表示出现错误或异常情况
///
/// ## Returns
///
/// 该函数实际上不会返回，因为调用进程会被终止。
/// 返回类型 `isize` 仅用于与系统调用接口保持一致。
///
/// ## Behavior
///
/// 1. 记录进程退出信息到内核日志
/// 2. 将当前任务标记为已退出状态
/// 3. 调度并切换到下一个就绪任务
/// 4. 如果没有其他任务，系统将处理所有任务完成的情况
///
/// ## Panics
///
/// 如果任务切换函数意外返回，会触发 panic（这在正常情况下不应该发生）
///
/// ## Examples
///
/// 从用户态调用：
/// ```c
/// exit(0);  // 正常退出
/// exit(1);  // 异常退出
/// ```
pub fn sys_exit(exit_code: i32) -> isize {
    println!("[kernel] Application exited with code {}", exit_code);
    exit_current_and_run_next();
    panic!("Unreachable in sys_exit!");
}

/// 系统调用：让出 CPU 时间片
///
/// 实现 `sched_yield(2)` 系统调用，当前任务主动让出 CPU，
/// 允许调度器选择其他就绪任务运行。这是一种协作式多任务的实现方式。
///
/// ## Returns
///
/// 成功时返回 0。当任务重新获得 CPU 时间片时，会从此处继续执行。
///
/// ## Behavior
///
/// 1. 将当前任务状态从 `Running` 改为 `Ready`
/// 2. 调度器选择下一个就绪任务运行
/// 3. 执行任务上下文切换
/// 4. 当前任务将来重新被调度时，从此函数返回
///
/// ## Use Cases
///
/// - 长时间运行的任务主动让出 CPU，提高系统响应性
/// - 等待某些条件满足时的忙等待优化
/// - 实现用户空间的协作式多任务
///
/// ## Examples
///
/// 从用户态调用：
/// ```c
/// while (condition) {
///     yield();  // 避免长时间占用 CPU
/// }
/// ```
pub fn sys_yield() -> isize {
    suspend_current_and_run_next();
    0
}

/// 系统调用：获取系统时间
///
/// 实现获取系统运行时间的系统调用，返回自系统启动以来经过的毫秒数。
/// 这是 `gettimeofday(2)` 的简化版本。
///
/// ## Returns
///
/// 返回系统启动以来经过的毫秒数（作为 `isize` 类型）
///
/// ## Precision
///
/// 时间精度取决于系统时钟频率和定时器实现，通常为毫秒级精度。
///
/// ## Use Cases
///
/// - 测量代码执行时间
/// - 实现定时器和超时机制
/// - 性能分析和基准测试
/// - 随机数生成的种子
///
/// ## Examples
///
/// 从用户态调用：
/// ```c
/// long start_time = get_time();
/// // 执行某些操作
/// long end_time = get_time();
/// long elapsed = end_time - start_time;  // 计算耗时
/// ```
///
/// ## Note
///
/// 返回的时间值可能会在长时间运行后溢出，调用者应该考虑处理这种情况。
pub fn sys_get_time() -> isize {
    get_time_ms() as isize
}

/// 系统调用：调整程序断点（堆管理）
///
/// 实现 `sbrk(2)` 系统调用，用于动态调整当前进程的堆大小。
/// 这是实现 `malloc`、`free` 等动态内存分配函数的基础系统调用。
///
/// ## Arguments
///
/// * `size` - 堆大小的变化量（字节）
///   - **正数**: 扩展堆空间，增加可用内存
///   - **负数**: 收缩堆空间，释放内存
///   - **零**: 查询当前程序断点位置，不改变堆大小
///
/// ## Returns
///
/// - **成功**: 返回调整前的程序断点地址（旧的堆顶地址）
/// - **失败**: 返回 -1，可能的失败原因：
///   - 内存不足，无法分配新的物理页面
///   - 试图收缩到堆底部以下
///   - 地址空间操作失败
///
/// ## 堆管理机制
///
/// ```text
/// 进程地址空间中的堆布局：
/// ┌─────────────────┐ ← heap_bottom (fixed)
/// │                 │
/// │  Allocated Heap │   ← Memory usable by user
/// │     Space       │
/// ├─────────────────┤ ← program_brk (variable, old value)
/// │                 │
/// │   New Space     │   ← New space when size > 0
/// │                 │
/// ├─────────────────┤ ← program_brk (variable, new value)
/// │   Unallocated   │
/// │      Space      │
/// └─────────────────┘
/// ```
///
/// ## 实现细节
///
/// 1. **委托处理**: 调用 `change_program_brk()` 执行实际的堆调整
/// 2. **页面对齐**: 底层自动处理页面边界对齐
/// 3. **物理内存**: 自动分配/释放物理页帧
/// 4. **地址空间**: 更新进程的虚拟地址空间映射
///
/// ## 错误处理
///
/// - 返回 -1 表示操作失败
/// - 用户程序应该检查返回值并适当处理错误
/// - 失败后堆状态保持不变
///
/// ## 使用场景
///
/// - **动态内存分配**: `malloc()` 的底层实现
/// - **内存池管理**: 自定义内存分配器
/// - **垃圾收集**: 动态调整堆大小
/// - **性能优化**: 预分配大块内存减少系统调用次数
///
/// ## Examples
///
/// 从用户态调用：
/// ```c
/// // 扩展堆空间 1KB
/// void *old_brk = sbrk(1024);
/// if (old_brk == (void*)-1) {
///     // 分配失败，处理错误
///     return -1;
/// }
///
/// // 查询当前堆顶
/// void *current_brk = sbrk(0);
///
/// // 收缩堆空间 512 字节
/// void *prev_brk = sbrk(-512);
/// ```
///
/// ## Security Notes
///
/// - 系统会自动验证内存访问权限
/// - 防止堆与栈重叠
/// - 确保不会访问其他进程的内存
///
/// ## Performance Considerations
///
/// - 频繁的小幅调整可能导致性能问题
/// - 建议批量分配以减少系统调用开销
/// - 页面分配/释放涉及内核态切换，有一定开销
pub fn sys_sbrk(size: i32) -> isize {
    if let Some(old_brk) = change_program_brk(size) {
        old_brk as isize
    } else {
        -1
    }
}
